# Мини-каталог фруктов

# Создаём словарь: ключ — название фрукта, значение — его цена
catalog = {
    "apple": 2.5,
    "banana": 1.8,
    "orange": 3.0,
    "mango": 4.5,
    "grape": 3.7
}

# Приветственное сообщение
print("Добро пожаловать в фруктовый магазин!")

# Показываем доступные фрукты
print("Доступные фрукты:", ", ".join(catalog.keys()))

# Просим пользователя ввести название фрукта
fruit = input("Введите название фрукта: ").lower()

# Проверяем, есть ли фрукт в каталоге
if fruit in catalog:
    # Если есть — выводим его цену
    print(f"Цена на {fruit} составляет ${catalog[fruit]}")
else:
    # Если нет — сообщаем, что товара нет
    print("Извините, такого фрукта нет в наличии.")